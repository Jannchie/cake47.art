function formatMessage(template: string, params?: Record<string, string | number>) {
  if (!params) {
    return template
  }
  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(params[key] ?? ''))
}

function clean(value: string | null | undefined) {
  return value?.trim() ?? ''
}

function fieldOrder(locale: Locale) {
  if (locale === 'zh-CN') {
    return ['zh', 'en', 'ja'] as const
  }
  if (locale === 'ja') {
    return ['ja', 'en', 'zh'] as const
  }
  return ['en', 'zh', 'ja'] as const
}

export function useAdminI18n() {
  const { locale, locales, setLocale } = useLocaleState()

  function t(key: AdminMessageKey, params?: Record<string, string | number>) {
    return formatMessage(adminMessages[locale.value][key] ?? adminMessages.en[key], params)
  }

  function localeName(value: Locale) {
    return localeNames[value]
  }

  function pickLocalized(values: { en?: string | null; zh?: string | null; ja?: string | null }) {
    for (const key of fieldOrder(locale.value)) {
      const value = clean(values[key])
      if (value) {
        return value
      }
    }
    return ''
  }

  function localizedCategoryLabel(item: LocalizedName) {
    return pickLocalized({ en: item.labelEn, zh: item.labelZh, ja: item.labelJa }) || clean(item.id)
  }

  function localizedSeriesName(item: LocalizedName) {
    return pickLocalized({
      en: item.nameEn ?? item.seriesNameEn ?? item.primarySeriesNameEn,
      zh: item.nameZh ?? item.seriesNameZh ?? item.primarySeriesNameZh,
      ja: item.nameJa ?? item.seriesNameJa ?? item.primarySeriesNameJa,
    }) || clean(item.id)
  }

  function localizedSeriesDescription(item: LocalizedName) {
    return pickLocalized({
      en: item.descriptionEn,
      zh: item.descriptionZh,
      ja: item.descriptionJa,
    })
  }

  function localizedArtworkTitle(item: LocalizedName, fallback?: string) {
    return pickLocalized({
      en: item.titleEn,
      zh: item.titleZh,
      ja: item.titleJa,
    }) || fallback || localizedSeriesName(item)
  }

  function localizedArtworkDescription(item: LocalizedName) {
    return pickLocalized({
      en: item.descriptionEn,
      zh: item.descriptionZh,
      ja: item.descriptionJa,
    })
  }

  return {
    locale,
    locales,
    setLocale,
    t,
    localeName,
    pickLocalized,
    localizedCategoryLabel,
    localizedSeriesName,
    localizedSeriesDescription,
    localizedArtworkTitle,
    localizedArtworkDescription,
  }
}
