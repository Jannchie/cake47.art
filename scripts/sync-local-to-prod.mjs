#!/usr/bin/env node
import { createClient } from '@libsql/client'
import { spawnSync } from 'node:child_process'
import { existsSync, readFileSync } from 'node:fs'
import { mkdir, readdir, stat, writeFile } from 'node:fs/promises'
import { extname, join, relative, resolve } from 'node:path'

const businessTables = [
  'categories',
  'artworks',
  'series',
  'artwork_series_links',
  'home_slots',
]

const deleteOrder = [
  'home_slots',
  'artwork_series_links',
  'series',
  'artworks',
  'categories',
]

const insertOrder = [
  'categories',
  'artworks',
  'series',
  'artwork_series_links',
  'home_slots',
]

const rootDir = process.cwd()
const localDbPath = resolve(rootDir, process.env.LOCAL_DB_PATH || '.data/db/sqlite.db')
const localBlobDir = resolve(rootDir, process.env.LOCAL_BLOB_DIR || '.data/blob')
const wranglerConfigPath = resolve(rootDir, process.env.WRANGLER_CONFIG || '.output/server/wrangler.json')
const d1Binding = process.env.PROD_D1_BINDING || 'DB'
const allowProdSync = process.env.ALLOW_PROD_SYNC === '1'
const timestamp = new Date().toISOString().replace(/[-:]/g, '').replace(/\..+/, '').replace('T', '-')
const outDir = resolve(rootDir, '.tmp', `prod-sync-${timestamp}`)
const generatedSqlPath = join(outDir, 'local-data.sql')
const backupSqlPath = join(outDir, 'prod-backup-before-sync.sql')

function log(message) {
  console.log(message)
}

function fail(message) {
  console.error(`Error: ${message}`)
  process.exit(1)
}

function quoteIdent(value) {
  return `"${value.replaceAll('"', '""')}"`
}

function sqlValue(value) {
  if (value === null || value === undefined) {
    return 'NULL'
  }
  if (typeof value === 'number') {
    if (!Number.isFinite(value)) {
      fail(`Cannot serialize non-finite number: ${value}`)
    }
    return String(value)
  }
  if (typeof value === 'bigint') {
    return String(value)
  }
  if (typeof value === 'boolean') {
    return value ? '1' : '0'
  }
  if (value instanceof Uint8Array) {
    return `X'${Buffer.from(value).toString('hex')}'`
  }
  return `'${String(value).replaceAll("'", "''")}'`
}

function mimeFromPath(filePath) {
  switch (extname(filePath).toLowerCase()) {
    case '.avif':
      return 'image/avif'
    case '.gif':
      return 'image/gif'
    case '.jpeg':
    case '.jpg':
      return 'image/jpeg'
    case '.png':
      return 'image/png'
    case '.webp':
      return 'image/webp'
    default:
      return 'application/octet-stream'
  }
}

function run(command, args) {
  log(`$ ${[command, ...args].join(' ')}`)
  if (!allowProdSync) {
    return
  }

  const result = spawnSync(command, args, {
    cwd: rootDir,
    stdio: 'inherit',
    env: process.env,
  })

  if (result.status !== 0) {
    fail(`Command failed: ${command} ${args.join(' ')}`)
  }
}

function readWranglerConfig() {
  if (!existsSync(wranglerConfigPath)) {
    if (allowProdSync) {
      fail(`Missing ${relative(rootDir, wranglerConfigPath)}. Run pnpm build:cloudflare first.`)
    }
    return null
  }

  const raw = JSON.parse(readFileSync(wranglerConfigPath, 'utf8'))
  const d1 = raw.d1_databases?.find(database => database.binding === d1Binding)
  const r2 = raw.r2_buckets?.find(bucket => bucket.binding === 'BLOB') || raw.r2_buckets?.[0]
  return {
    d1DatabaseId: d1?.database_id,
    r2BucketName: process.env.NUXT_HUB_CLOUDFLARE_R2_BUCKET || r2?.bucket_name,
  }
}

async function getColumns(db, table) {
  const result = await db.execute(`pragma table_info(${quoteIdent(table)})`)
  const columns = result.rows.map(row => String(row.name))
  if (columns.length === 0) {
    fail(`Table ${table} is missing from ${relative(rootDir, localDbPath)}`)
  }
  return columns
}

async function getRows(db, table) {
  const result = await db.execute(`select * from ${quoteIdent(table)}`)
  return result.rows
}

async function generateSql() {
  const db = createClient({ url: `file:${localDbPath}` })
  const lines = [
    '-- Generated by scripts/sync-local-to-prod.mjs',
    '-- Business data only; _hub_migrations is intentionally not synchronized.',
    '',
  ]
  const summary = {}

  for (const table of deleteOrder) {
    lines.push(`DELETE FROM ${quoteIdent(table)};`)
  }
  lines.push('')

  for (const table of insertOrder) {
    const columns = await getColumns(db, table)
    const rows = await getRows(db, table)
    summary[table] = rows.length
    if (rows.length === 0) {
      continue
    }

    const columnSql = columns.map(quoteIdent).join(', ')
    for (const row of rows) {
      const values = columns.map(column => sqlValue(row[column])).join(', ')
      lines.push(`INSERT INTO ${quoteIdent(table)} (${columnSql}) VALUES (${values});`)
    }
    lines.push('')
  }

  lines.push('')

  db.close()
  return { sql: lines.join('\n'), summary }
}

async function listBlobFiles(dir) {
  if (!existsSync(dir)) {
    return []
  }

  const entries = await readdir(dir, { withFileTypes: true })
  const files = []
  for (const entry of entries) {
    const fullPath = join(dir, entry.name)
    if (entry.isDirectory()) {
      files.push(...await listBlobFiles(fullPath))
    } else if (entry.isFile() && !entry.name.endsWith('.$meta.json')) {
      const fileStat = await stat(fullPath)
      files.push({
        filePath: fullPath,
        key: relative(localBlobDir, fullPath).split('\\').join('/'),
        size: fileStat.size,
        contentType: mimeFromPath(fullPath),
      })
    }
  }
  return files.sort((a, b) => a.key.localeCompare(b.key))
}

async function main() {
  if (!existsSync(localDbPath)) {
    fail(`Missing local database: ${relative(rootDir, localDbPath)}`)
  }

  await mkdir(outDir, { recursive: true })
  const config = readWranglerConfig()
  const r2BucketName = config?.r2BucketName

  if (allowProdSync && !r2BucketName) {
    fail('Missing R2 bucket name. Set NUXT_HUB_CLOUDFLARE_R2_BUCKET or rebuild .output/server/wrangler.json.')
  }

  const { sql, summary } = await generateSql()
  await writeFile(generatedSqlPath, sql)

  const blobFiles = await listBlobFiles(localBlobDir)
  const blobBytes = blobFiles.reduce((total, file) => total + file.size, 0)

  log('')
  log(allowProdSync ? 'PROD SYNC ENABLED' : 'DRY RUN ONLY')
  log(`Local DB: ${relative(rootDir, localDbPath)}`)
  log(`Local blobs: ${relative(rootDir, localBlobDir)}`)
  log(`Generated SQL: ${relative(rootDir, generatedSqlPath)}`)
  log(`Wrangler config: ${existsSync(wranglerConfigPath) ? relative(rootDir, wranglerConfigPath) : 'missing'}`)
  log(`D1 binding: ${d1Binding}`)
  log(`R2 bucket: ${r2BucketName || 'unknown'}`)
  log('')
  log('Rows to replace:')
  for (const table of businessTables) {
    log(`  ${table}: ${summary[table] ?? 0}`)
  }
  log(`Blob objects to upload: ${blobFiles.length} (${Math.round(blobBytes / 1024)} KiB)`)
  log('')

  if (!allowProdSync) {
    log('No production changes were made.')
    log('Review the generated SQL, then run with ALLOW_PROD_SYNC=1 to execute:')
    log('  ALLOW_PROD_SYNC=1 pnpm sync:prod')
    return
  }

  log('Step 1/3: Backing up remote D1...')
  run('pnpm', [
    'exec',
    'wrangler',
    'd1',
    'export',
    d1Binding,
    '--remote',
    '--config',
    wranglerConfigPath,
    '--output',
    backupSqlPath,
    '--skip-confirmation',
  ])

  log('Step 2/3: Uploading blob objects to R2...')
  for (const file of blobFiles) {
    run('pnpm', [
      'exec',
      'wrangler',
      'r2',
      'object',
      'put',
      `${r2BucketName}/${file.key}`,
      '--file',
      file.filePath,
      '--remote',
      '--config',
      wranglerConfigPath,
      '--content-type',
      file.contentType,
      '--force',
    ])
  }

  log('Step 3/3: Replacing remote D1 business data...')
  run('pnpm', [
    'exec',
    'wrangler',
    'd1',
    'execute',
    d1Binding,
    '--remote',
    '--config',
    wranglerConfigPath,
    '--file',
    generatedSqlPath,
    '--yes',
  ])

  log('')
  log('Production sync complete.')
  log(`Backup saved to ${relative(rootDir, backupSqlPath)}`)
}

main().catch((error) => {
  fail(error?.stack || error?.message || String(error))
})
